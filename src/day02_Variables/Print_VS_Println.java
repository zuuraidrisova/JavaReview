package day02_Variables;

public class Print_VS_Println {

    public static void main(String[] args) {

        System.out.println(""); //it prints and breaks the line

        System.out.print("");// this prints but does not break the line

        System.out.println("Hello World");

        System.out.print("Hello");//does not break the line


        System.out.println("World");

        System.out.println("HELLO");// new line
    }
}
