package day42_InheritanceContinue;

public class AccessModifiers {
    /*

    Access Modifiers:

    public ==> accessible and visible to the world

    default ==> only visible in same package

    private ==> only visible in same class

    protected ==> visible in same package, and outside package only for sub classes

    public and protected can be inherited in any subclasses

           public == > protected ==> default ==> private

    Inheritance:

        sub class inheriting attributes and methods of super class

        sub class can inherit variables and methods which are visible

        one class can extend only one class

        one class can be extended by multiple classes

        Constructor cannot be inherited

        Super class cannot inherit

 sub class : inherits Public variables and methods from super class
                      Private variables and methods can never be inherited
                      Default can be inherited as long as subclasses are within the same package
                      Public and  protected can always be inherited in the subclasses, at anywhere



     */
}
