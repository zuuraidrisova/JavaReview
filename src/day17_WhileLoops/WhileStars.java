package day17_WhileLoops;

public class WhileStars {

    public static void main(String[] args) {

        int i = 1;

        while(i <= 100){

            System.out.print("* ");

            i++;
        }


        System.out.println();


        //rectangle

        System.out.println("* * * *");

        int k = 1;
        while(k <= 4){

            System.out.println("*     *");
            k++;
        }

        System.out.println("* * * *");



    }
}
