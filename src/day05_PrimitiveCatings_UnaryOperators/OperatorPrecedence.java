package day05_PrimitiveCatings_UnaryOperators;

public class OperatorPrecedence {

    public static void main(String[] args) {

        /*
        Operator Precedence:

        highest:  * ,/ , %
        lowest: +, -
         */

        System.out.println(3 * 2 / 4);
        System.out.println(3 + 2 * 4);


    }
}
