package day50_PolymorphismContinue;

public class Polymorphism {
     /*
Polymorphism:
    objects behave in meany forms

    occurs when parent class/interface is reference to child's class object

        reference type decides what can be accessible, what can be called

        when we call a method, it will always call overridden method in child class

        if method is not overridden, then original method in parent class will be executed

        if a method exist in both object type and reference type:
            if method is overridden then object type gets executed
            if method is not overridden then reference type gets executed
     */
}
